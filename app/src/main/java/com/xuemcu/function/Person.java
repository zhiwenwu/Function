public class Person {

    public String name;
    public int photo1;
    public int photo2;
    public int photo3;
    public String Text;

    public Person(String name, int photo1, int photo2, int photo3, String Text) {
        super();
        this.name = name;
        this.photo1 = photo1;
        this.photo2 = photo2;
        this.photo3 = photo3;
        this.Text = Text;
    }
}